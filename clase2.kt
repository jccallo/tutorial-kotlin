fun main() {
   // var es usado para declarar variables
   var firstName = "Armando"  
   var lastName = "Morales"

   // val es usado para declarar constantes
   val gender = "Masculino"          
   val country = "Peru"

   // interpolamos variables y constantes
   println("firstName = $firstName")
   println("lastName = $lastName")
   println("gender = $gender")
   println("country = $country")
}